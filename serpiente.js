class Serpiente 
{
    
    constructor(init, fin) 
    {
        this.init = init
        this.fin = fin

        if (init < fin) 
        {
            this.init = 0
            this.init = 0
        }
    }

    get init() 
    {
        return this.init
    }

    get fin() 
    {
        return this.fin
    }

} export default Serpiente